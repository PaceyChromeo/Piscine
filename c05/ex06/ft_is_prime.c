int		ft_is_prime(int nb)
{
	int prm;

	prm = 3;
	if (nb <= 1)
		return (0);
	if (nb == 2 || nb == 3)
		return (1);
	if (nb % 2 == 0 || nb % 3 == 0)
		return (0);
	if (nb == 2147483647)
		return (1);
	while ((prm * prm) <= nb)
	{
		if ((nb % prm) == 0)
			return (0);
		prm++;
	}
	return (1);
}
