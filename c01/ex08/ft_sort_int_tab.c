void	ft_sort_int_tab(int *tab, int size)
{
	int temp;
	int i;

	temp = 0;
	i = 1;
	while (i < size)
	{
		if (tab[i] < tab[i - 1])
		{
			temp = tab[i - 1];
			tab[i - 1] = tab[i];
			tab[i] = temp;
			i = 0;
		}
		i++;
	}
}
